<h3> L'essentiel à savoir pour un animal en bonne santé </h3>

<p> La vaccination </p>
Les premières vaccinations sont réalisées par notre vétérinaire au sein de l'association. Lors de l'adoption, l'équipe vous remettra le calendrier de vaccination de votre animal. La vaccination s'effectuera par la suite tous les ans.

<p> La vermifugation </p>
Afin de pallier aux différents parasites intestinaux chez l'animal, il est nécessaire de procéder à la vermifugation de ce dernier. Les six premiers mois, un traitement mensuel est réalisé. Ce soin doit être continuer tout au long de la vie de l'animal. Vous pourrez acheter le médicament dans les pharmacies ou les animalieres de votre ville.

<p> La stérilisation </p>
La stérilisation doit s'effectuer lors des 6 mois de l'animal. Lors de l'adoption, vous payez cette stérilisation qui est OBLIGATOIRE.

<p> L'alimentation </p>
La nourriture dépend de l'âge de votre animal. Veuillez à respecter la nourriture en fonction de l'âge de l'animal pour que la croissance de l'animal soit optimale.

<p> Les puces et tiques </p>
L'assocation fournit les premières doses de traitement de prévention anti-parasitaire pour tout animal ayant plus de 6 mois. Privilégiez les pipettes aux colliers.

<p> Le soin des oreilles </p>
Un nettoyage régulier permet de prévenir les risques d'otites et d'infections du à des parasites.

<p> L'hygiène dentaire </p>
Des dentifrices ou friandises permettant de protéger les dents existent. Des croquettes permettent également de prévenir l'apparition du tartre et des plaques


