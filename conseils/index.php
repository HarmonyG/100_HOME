<!-- Conseil avant d'adopter -->
<h3> Conseils post-adoption </h3>

<p> Adopter en association vous certifie d'avoir un animal en bonne santé: chaque animal est examiné par notre vétérinaire et fait l'objet d'analyses.
En effet, les animaux de l'association sont testés, vaccinés, pucés et stérilisés selon leur âge. Adopter par le biais d'une association c'est aussi être 
en règle au regard de la loi: chaque animal est identifié. De plus, l'équipe 100HOME apporte un service "après-vente" pour toutes informations complémentaires (doutes? Conseils etc.).</p>

<p>Adopter est un acte sérieux, c'est un contrat avec des responsabilités envers l'animal. 
Afin de préparer son arrivée et faciliter son adaptation, pensez à vous équiper au préalable: nourriture, accessoires, jouets etc. </p>

<p> Voici quelques règles que vous retrouverez sur votre contrat d'adoption :
Vous vous engagez à vous occupez de votre animal de façon responsable, le nourrir, l'aimer et prendre soin de lui et de sa santé tout au long de sa vie, 
de ne pas l'abandonner sans en avoir fait mention auprès de 100HOME, à accepter une ou plusieurs visites de l'association pour assurer le suivi post-adoption.</p>

<p> Il est nécessaire de se poser les bonnes questions avant une adoption afin d'éviter tout désagrément : Suis-je prêt à pouvoir m'engager sur le long terme? Ai-je assez 
de temps disponible pour m'en occuper? Ai-je les moyens financièrement? Puis-je m'organiser pour les vacances?</p> 

<p> Les papiers nécessaires pour une adoption: pièce d'identité, justificatif de domicile, collier/laisse et d'une cage de transport si vous adoptez un chat, lapin etc.


